    private void ShootNoise(Object stateInfo) {
      //hmidi is an IntPtr obtained via midiOutOpen or other means.
      byte[] data = new byte[4];
      data[0] = 0xC0;//change instrument, channel 1
      data[1] = 127;//gunshot
      uint msg = BitConverter.ToUInt32(data, 0);
      midiOutShortMsg(handle, msg);
      data[0] = 0x90;//note on, channel 0
      data[1] = 50;//pitch
      data[2] = 100;//velocity
      msg = BitConverter.ToUInt32(data, 0);
      midiOutShortMsg(handle, msg);
      // Try Doom
      Doom();
    }
    // FrameWork
    private void changeController(byte channel, byte controllerType, byte value) {
      byte[] data = new byte[4];
      data[0] = 0xb0;//note on
      data[1] = controllerType;//number
      data[2] = value;//number
      midiOutShortMsg(handle, BitConverter.ToUInt32(data, 0));
    }
    private void noteType(byte channel, byte sound) {
      byte[] data = new byte[4];
      data[0] = 0xc0;//note on
      data[1] = sound;//number
      midiOutShortMsg(handle, BitConverter.ToUInt32(data, 0));
    }
    private void pitchBend(byte channel, int value) {
      byte[] data = new byte[4];
      data[0] = 0xe0;//note on
      data[1] = (byte)(value >> 16);
      data[2] = (byte)(value >> 8);
      midiOutShortMsg(handle, BitConverter.ToUInt32(data, 0));
    }
    private void playNote(byte channel, byte noteNumber, byte velocity) {
      byte[] data = new byte[4];
      data[0] = 0x90; //note on
      data[1] = noteNumber;//pitch
      data[2] = velocity;//velocity
      midiOutShortMsg(handle, BitConverter.ToUInt32(data, 0));
    }
